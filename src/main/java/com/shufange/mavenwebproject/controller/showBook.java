package com.shufange.mavenwebproject.controller;


import com.shufange.mavenwebproject.model.Book;
import com.shufange.mavenwebproject.service.bookManageSvc;
import java.io.IOException;
import java.util.ArrayList;
import java.util.List;
import javax.ejb.EJB;
import javax.servlet.ServletException;
import javax.servlet.annotation.WebServlet;
import javax.servlet.http.HttpServlet;
import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;


@WebServlet(name = "showBook", urlPatterns = {"/showBook/"})
public class showBook extends HttpServlet {

    
    private final static bookManageSvc bookManageSvc = new bookManageSvc();

    /**
     * Handles the HTTP
     * <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {

        // get the customers from the database
        List<Book> books = bookManageSvc.showBook();
        // set the collection in the request scope        
        request.setAttribute("books", books);      
        // and forward the request to the appropriate view
        request.getRequestDispatcher("/showOffBook.jsp").forward(request, response);
    }

    /**
     * Handles the HTTP
     * <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        // no real post action for the show customers controller
        doGet(request, response);
    }

    /**
     * Returns a short description of the servlet.
     *
     * @return a String containing servlet description
     */
    @Override
    public String getServletInfo() {
        return "Short description";
    }

}